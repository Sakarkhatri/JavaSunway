package com.sunway.java;

public class SwitchCase {

	public static void main(String[]args) {
		int day = 8;
		switch (day) {
		case 1:
		System.out.println("monday");
		break;
		
		case 2:
		System.out.println("goose");
		break;

		case 5:
		System.out.println("day");
		break;
		
		case 7:
		System.out.println("Bad garde");
		break;
		
		case 8:
		System.out.println("If it prints 'Sunday' this is working ");
		break;
		}
		
		
	}
}
