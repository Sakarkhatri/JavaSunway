package com.ArrayList.java;

public class ClassesSunway {
private String type;
private int numberOfDesk;
private int numberOfStudents;
public ClassesSunway(String type, int noOfDesk, int numberOfStudents) {
	this.type = type;
	this.numberOfDesk = noOfDesk;
	this.numberOfStudents = numberOfStudents;
}
public String getType() {
	return type;
}
public void setType(String type) {
	this.type = type;
}
public int getNoOfDesk() {
	return numberOfDesk;
}
public void setNoOfDesk(int noOfDesk) {
	this.numberOfDesk = noOfDesk;
}
public int getNumberOfStudents() {
	return numberOfStudents;
}
public void setNumberOfStudents(int numberOfStudents) {
	this.numberOfStudents = numberOfStudents;
}


	




}

